<script lang="ts" setup>
import { nextTick, ref } from 'vue';
import { getMessages } from '@/api/aigc/conversation';
import Message from '@/views/chat/message/Message.vue';
import { ElDrawer, ElScrollbar, ElEmpty, ElButton } from 'element-plus';

const messageRef = ref();
const contentRef = ref();
const loading = ref(true);
const showModel = ref(false);
const info = ref<any>({});
const messages = ref<any>([]);

async function show(row: any) {
  showModel.value = true;
  await nextTick();

  info.value = row;
  messages.value = await getMessages(row.id);
  loading.value = false;
}

async function handleDelete(row) {
  console.log('del', row);
}

defineExpose({ show });
</script>

<template>
  <el-drawer
    v-model="showModel"
    :size="1000"
    direction="rtl"
    :title="info.title"
  >
    <div ref="contentRef">
      <el-scrollbar ref="messageRef" height="calc(100vh - 120px)">
        <Message
          v-for="(item, index) of messages"
          :key="index"
          :date-time="item.createTime"
          :error="false"
          :inversion="item.role !== 'assistant'"
          :loading="loading"
          :text="item.message"
          @delete="handleDelete(item)"
        />
      </el-scrollbar>
    </div>
    <el-empty
      v-if="messages.length == 0"
      class="mt-5"
      description="此会话还没有聊天信息"
    />

    <template #footer>
      <el-button @click="showModel = false">关闭</el-button>
    </template>
  </el-drawer>
</template>

